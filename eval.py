import numpy as np
import matplotlib.pyplot as plt




def plots_correlation(D, f, Dp, flag):
    # plot correlations
    plt.figure()
    plt.plot(D[:1000], Dp[:1000], 'rx', markersize=5)
    plt.xlim(0, 0.005)
    plt.ylim(0, 0.3)
    plt.xlabel('D')
    plt.ylabel('Dp')
    plt.gcf()
    plt.savefig('plots/inputDDp' + flag + '.png')
    plt.ion()
    plt.show()
    plt.figure()
    plt.plot(f[:1000], Dp[:1000], 'rx', markersize=5)
    plt.xlim(0, 0.6)
    plt.ylim(0, 0.3)
    plt.xlabel('f')
    plt.ylabel('Dp')
    plt.gcf()
    plt.savefig('plots/inputfDp' + flag + '.png')
    plt.ion()
    plt.show()
    plt.figure()
    plt.plot(D[:1000], f[:1000], 'rx', markersize=5)
    plt.xlim(0, 0.005)
    plt.ylim(0, 0.6)
    plt.xlabel('D')
    plt.ylabel('f')
    plt.gcf()
    plt.savefig('plots/inputDf' + flag + '.png')
    plt.ion()
    plt.show()


